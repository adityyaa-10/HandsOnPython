# Python Identifiers --> Rules

# Use only alpha-numeric characters and underscores 
# Never start with a number
# Use underscores to separate two words (Not necessary)
# Keywords such as int, float, main, if, for, etc. can not be identifiers.

# Examples --> 

age = 24
name = "Aditya Pandey"
_id = 'xyz123'
# Above three are valid identifiers

# 2name = 'adi' --> Invalid identifier
# marks$ --> Invalid identifier because of $

